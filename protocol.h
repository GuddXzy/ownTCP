/* ---- 协议头 ---- */
typedef struct {
    uint16_t magic;   /* 固定 0xABCD                     */
    uint16_t crc16;   /* payload 的 CRC-16/MODBUS 校验值 */
    uint32_t len;     /* payload 长度, 网络字节序         */
    uint32_t type;    /* 消息类型, 网络字节序             */
} MsgHeader;          /* 共 12 字节                       */

/* ---- 业务消息体 ---- */
typedef struct {
    char username[32];
    char password[32];
} LoginMsg;

typedef struct {
    char to_uid[32];
    char from_uid[32];
    char content[960];
} ChatMsg;

