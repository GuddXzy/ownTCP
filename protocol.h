/*
 *  * ownTCP 自定义二进制协议 v2
 *   *
 *    * 帧结构:
 *     * +--------+--------+--------+--------+------------------+
 *      * | magic  | crc16  |  len   |  type  |    payload       |
 *       * | 2字节  | 2字节  | 4字节  | 4字节  |   len字节        |
 *        * +--------+--------+--------+--------+------------------+
 *         *
 *          * magic:   固定 0xABCD，用于帧定界，快速识别有效数据包
 *           * crc16:   CRC-16/MODBUS，对 payload 部分计算，端到端校验
 *            * len:     payload 长度（网络字节序）
 *             * type:    消息类型（网络字节序）
 *              * payload: 实际数据，长度由 len 指定
 *               *
 *                * 面试要点:
 *                 * Q: 为什么TCP已经有校验还要加CRC？
 *                  * A: TCP校验保证传输层可靠，CRC保证应用层数据完整。
 *                   *    程序bug、内存越界、序列化错误等TCP检测不到。
 *                    *
 *                     * Q: 为什么用CRC-16而不是CRC-32？
 *                      * A: 嵌入式端(ESP8266)资源有限，CRC-16已足够检测常见错误，
 *                       *    且MODBUS是工业标准，兼容性好。
 *                        *
 *                         * Q: magic number的作用？
 *                          * A: 帧定界。在字节流中快速定位帧起始位置，
 *                           *    防止解析器在数据错位时误解析垃圾数据。
 *                            */

#define PROTO_MAGIC  0xABCD

/* ---- 消息类型 ---- */
#define MSG_TYPE_DATA           1
